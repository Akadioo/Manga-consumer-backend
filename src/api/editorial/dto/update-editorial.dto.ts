import { ApiProperty } from '@nestjs/swagger';
import { IsOptional, IsString, IsEmail, IsAlpha } from 'class-validator';

export class UpdateEditorialDto {
  @ApiProperty({ required: false })
  @IsOptional()
  @IsString()
  name?: string;

  @ApiProperty({ required: false })
  @IsOptional()
  @IsString()
  website?: string;

  @ApiProperty({ required: false })
  @IsOptional()
  @IsAlpha()
  country?: string;

  @ApiProperty({ required: false })
  @IsOptional()
  @IsString()
  phone?: string;

  @ApiProperty({ required: false })
  @IsOptional()
  @IsEmail()
  contact_email?: string;
}
